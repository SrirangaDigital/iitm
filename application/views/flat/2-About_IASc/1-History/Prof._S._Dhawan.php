<p>
	<strong>Name:</strong> DHAWAN, Satish<br />
	Elected 1970;<br /> 
	<strong>Council Service:</strong> 1971-91;<br />
	<strong>President:</strong> 1977-79;<br /> 
	<strong>Vice-President:</strong> 1974-76<br />
	Member, Space Commission,<br />
	Dept. of Space, Bengaluru<br />
	<strong>Date of birth:</strong> 25 September 1920, Srinagar, Jammu &amp; Kashmir<br />
	<strong>Date of death:</strong> 3 January 2002<br />
</p>

<p>Edn.: Punjab Univ.: BA 38, MA 41, BE 44; Univ. of Minnesota: MS, 47; California Inst. of Tech., USA: AE 49, PhD. 51. Career: IISC, Bengaluru, Aero Engg. Dept.: Asst. Prof. 51-55 Prof. &amp; Head 55-62, Director 63-81; Dept. of Space: Secy. and Chairman, Space Commission 72-84, Member 85. Fellow, INSA, Council Space Commission, Pres. Royal Aeronautical Soc. of India; Foreign Associate; US National Academy of Engg. Padma Shri 66; Cal Tech. Distinguished Alumnus 69; Padma Bhushan 71; Univ. of Roorkee 72, Cran-field Inst. of Tech., UK 75, Punjab Univ. 78, IIT, Madras 81, Delhi Univ. 84: DSc. h c. Padma Vibhushan 81; INSA Aryabhata Medal 83; G. M. Modi Award 88. Sc. Work in Aeronautics and Fluid mechanics. Conducted early experimental studies of shock/boundary-layer interaction. Made direct measurements of skin friction on a flat plate by a novel balance. Established the first high-speed wind tunnels in India at IISc. Investigated transition from laminar to turbulent flow and the reverse process in several flow configurations. Formulated and led the national space programme from 1972 to 1984.
</p>

