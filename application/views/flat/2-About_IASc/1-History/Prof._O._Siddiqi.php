<p>
	<strong>Name:</strong> SIDDIQI, Obaid<br />
	Elected 1968;
	<strong>Council Service:</strong> 1974-91;<br />
	<strong>President:</strong> 1986-88;<br />
	<strong>Vice-President:</strong> 1983-85.<br />
	Hon. Professor, TIFR National Centre for Biological Sciences, Bengaluru<br />
	<strong>Date of birth:</strong> 7 January 1932, Basti, Uttar Pradesh<br />
	<strong>Date of death:</strong> 26 July 2013<br />
</p>

<p>Edn.: Aligarh Muslim Univ.: MSc. 53; Univ. of Glasgow, UK: PhD. 61. Career: Aligarh Muslim Univ.: Lecturer 54-57; IARI, Delhi: Res. Scholar 57-58; Univ. of Glasgow, Dept. of Genetics: 50-61; Cold Spring Harbor Lab., USA: Lallor Foundation Fellow 61; Pennsylva-nia Univ., USA: Post doctoral Fellow 61-62; TIFR, Bombay, Molec-ular Biology Unit: Fellow, Assoc. Prof., Prof., Eminent Professor 62-?; TIFR, National Centre for Biol. Sc., Bengaluru: Director 93 ; Yale Univ., Dept. of Biophysics: Vis. Associate 66; Cambridge Inst. of Tech., USA: Vis. Prof. 68, 70-71; Gosney Fellow 72-73, Sherman Fairchild, Distinguished Scholar 81-82, 86; Jawaharlal Nehru Centre for Advanced Scientific Research, Banga-lore: Hon. Prof.; National Inst. of Advanced Studies, Bengaluru: Sr. Associate. Fellow, Royal Society of London, INSA, TWAS, Maha-rashtra Academy of Sc., National Academy of Sciences. Bhatnagar Award 76; Padma Bhushan 84; Birla Smarak Kosh National Award 89; Goyal Prize 91; INSA Aryabhata Medal 92; Aligarh Univ., BHU: DSc. h c. Sc. Work in Molecular and Behavioural Genetics. Research covers microbial genetics and genetic neurobiology. With Garen discovered the suppressor of 'nonsense' mutations, which led to the elucidation of chain-terminating triplets in protein synthesis. Did some of the early experiments which show that recombinant bacteria inherit labelled DNA of biparental origin. At TIFR demonstrated that DNA transfer can be associated from replication and that recombinants arise from unreplicated DNA. In early seventies, turned to behavioural genetics and neurobiology of Drosophila. Carried out with the group pioneeering work on taste and smell in the fruitfly, opening up the prospect of integrated genetic and neurobiological study of chemosensory perception.</p>

