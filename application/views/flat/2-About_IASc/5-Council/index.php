<h1>Council</h1>
<h2>&nbsp;</h2>

<p>The affairs and property of the Academy are administered by a Council of 20, consisting of a President, four Vice-Presidents, a Treasurer, two Secretaries, and twelve other members. The Council, with a term of three years, is elected by the Fellows triennially.</p>
