<h1>Jubilee Chair</h1>
<h2>&nbsp;</h2>

<p>
	<b>The Jubilee Professorship</b> was instituted during the Platinum Jubilee Year of the Academy (2009). General guidelines are as follows:
</p>

<ol>
	<li class="gap-above-small">Eminent scientists from any country and from any scientific discipline can be invited to occupy the Jubilee Professorship for a minimum period of two weeks and up to one month.</li>
	<li class="gap-above-small">The Jubilee Professor may pursue any scientific activity and may be based in any laboratory or Institution in India. The Professor is expected to visit smaller towns, universities for delivering lectures, holding seminars and interacting with students, teachers and researchers or for scientific collaboration.</li>
	<li class="gap-above-small">The Jubilee Professor is entitled to an honorarium of Rs 80,000/- per month in addition to local hospitality for the Jubilee Professor and spouse/companion.</li>
	<li class="gap-above-small">The Academy will provide one business class airfare from the place of residence to India and back or two economy class excursion fares if accompanied by spouse/companion.</li>
	<li class="gap-above-small">All travel expenses of the Jubilee Professor within India will be met by the Academy.</li>
</ol>

