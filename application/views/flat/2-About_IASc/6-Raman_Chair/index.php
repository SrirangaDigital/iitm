<h1>Raman Chair</h1>
<h2>&nbsp;</h2>

<p>The <strong>Raman Chair</strong> was instituted in 1972 by the Government of India to commemorate the memory of the founder of the Academy, Sir C. V. Raman. Eminent scientists are invited by the Council of the Academy to occupy the Chair, for periods of between six weeks and six months. Raman Professors who have occupied the Chair are:</p>

