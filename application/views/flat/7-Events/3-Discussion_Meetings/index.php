<h1>Discussion Meetings</h1>
<h2>&nbsp;</h2>

<p>Academy supports discussion meetings of small groups of scientists from within India and outside organized by a Fellow at a venue of the Fellow's choice. Travel support is not available for attendees from outside the country. A special venue is Orange County resort in the picturesque hill district of Kodagu near Bengaluru, where Academy has two time slots in every calendar year.</p>
