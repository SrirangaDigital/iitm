<h2>Publications of the Indian Academy of Sciences</h2>

<p>In addition to the ten scientific journals that are brought out by the Academy, there are a number of books and reports that we publish each year.</p>
<p>The IASc newsletter Patrika comes out twice a year, and  is a record of all the major activities, events and initiatives of the Academy.</p>

