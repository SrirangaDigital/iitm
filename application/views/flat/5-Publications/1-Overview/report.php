<h1>Report on University Science Education </h1>
<h2>&nbsp;</h2>
<h3>1. Introduction</h3>
<p>In recent years the Academy has felt increasingly concerned with the state of university education in science in the country. It has been widely realized that without a proper base in education it will not be possible to promote progress in science to attain the excellence that is a major concern of the Academy.</p>
