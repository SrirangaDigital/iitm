<h3>2. DIMENSIONS AND COMMON PERCEPTIONS OF THE PROBLEM</h3>
<p>Although the main focus of this document is university science education particularly at the undergraduate level, it is realized that undergraduate education is part of a continuum, starting with education at the school level and going on to research in science.</p>
