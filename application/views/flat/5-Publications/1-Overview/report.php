<h3>5. RECOMMENDATIONS </h3>
<p>The recommendations are presented here in two groups: first those that involve national bodies, agencies, the educational system, research laboratories and industry; second, those that depend mainly on initiatives to be taken by the Academy. Some overlap with the views presented in Section 4 is retained to make the recommendations self-contained.</p>
