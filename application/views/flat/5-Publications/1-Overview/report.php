<h3>4. GENERAL VIEWS OF THE ACADEMY PANEL</h3>
<p>In its own discussions the Academy Panel paid careful attention to many aspects of the overall problems of science education at both undergraduate and postgraduate levels. This was against the background of the knowledge of the efforts of other groups and agencies, such as those briefly described in Section 3.</p>
