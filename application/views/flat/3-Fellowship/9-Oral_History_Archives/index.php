<h1>Oral history archive</h1>
<h2>Audio and video documentaries</h2>

<p>In the Academy Council meeting held in December 2011, the proposal of creating an 'Oral history archive' was discussed and finalized for implementation. The archive is intended to be a repository of audio and video documentaries of some of the leading scientists of the country from among Academy's fellowship. It is hoped that the documented glimpses of experiences and careers of eminent personalities, while being a historical record, will also be of inspirational value to new generations of students and scientists. To start with, Academy undertook the production of video memoirs on Professor Roddam Narasmha and Dr D. Balasubramanian, two past presidents of the Academy.</p>
