<h1>Associateship</h1>
<h2>Associate of the Indian Academy of Sciences</h2>

<p>This programme was initiated by the Academy in 1983 to identify and encourage promising young scientists. The Associateship is tenable for a maximum period of five years until the age of 35 or a minimum period of three years. During this period the Associates can attend meetings of the Academy and receive two of the Academy journals of their choice. They are not required to pay any dues to the Academy. The total number of Associates is limited to 100.</p>
<h3>Information about nomination to the Associateship</h3>
<ul>
    <li>Nominations proposing candidates below the age of 35 years can be submitted by Fellows to reach the Academy office by 30 April each year. For selection to the Associateship during 2016, candidates born after 31-12-1981 are eligible to be nominated. The nomination should include four sets of: nomination form along with a brief write-up, curriculum vitae, list of publications, a brief statement highlighting the research contributions and reprints of the five most important papers of the candidate. The validity of the nomination is one year.</li>
    <li>Nomination forms are provided only to Fellows and are not made available on the Academy website.</li>
    <li>Only young scientists pursuing research in India are eligible for nomination.</li>
</ul>

