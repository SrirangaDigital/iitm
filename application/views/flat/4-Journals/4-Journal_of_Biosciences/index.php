<h1>Journal of Biosciences</h1>
<h2>&nbsp;</h2>
<h3>History</h3>
<p>The <em>Journal of Biosciences</em> began in 1934 as a part of the <em>Proceedings of the Indian Academy of Sciences – Section B</em> until it was split in 1978 from Proceedings – Section B to become Proceedings – Animal Sciences, Proceedings --  Plant Sciences and Proceedings – Experimental Biology. In 1979 Proceedings -- Experimental Biology  was named Journal of Biosciences. In  1991,  Animal Sciences and Plant Sciences merged with experimental Biology to comprise one journal and retained the name Journal of Biosciences.</p>
<h3>Aim and Scope</h3>
<p>The journal covers all areas of biology and is considered the premier journal in the country within its scope. It publishes research and reviews, series articles, perspectives, clipboards, commentaries, and short communications. Special theme issues are also published.</p>
<h3>Co-publishing Arrangement with Springer</h3>
<p><em>Journal of Biosciences</em> is now distributed in print outside India and online worldwide by <a target="_blank" href="http://www.springer.com">Springer</a>, co-publisher of the journal together with the Indian Academy of Sciences. On Springer, <em>Journal of Biosciences</em> <a target="_blank" href="http://www.springer.com/12038">(www.springer.com/12038)</a> is part of <a target="_blank" href="http://www.springerlink.com">SpringerLink</a>, one of the world's leading interactive databases of high quality STM journals, book series, books, reference works and online archives collection. Print subscriptions within India and open online access will continue to be served from Indian Academy of Sciences.</p>
