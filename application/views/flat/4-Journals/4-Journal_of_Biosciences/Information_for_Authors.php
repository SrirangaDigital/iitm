<h1>Information for Authors</h1>
<h2>Journal of Biosciences</h2>

<p>(Manuscripts not conforming to the following specifications will be returned)</p>
<p>The submission of a paper by a set of authors will be held to imply that it represents the results of their original research not previously published; that it is not under consideration for publication elsewhere; and that if accepted for <em>Journal of Biosciences</em> it will not be published elsewhere. Please read the <a href="<?=RESOURCES_URL?>Journals/Information_for_Authors/pactform.pdf" target="_blank">PACT FORM</a> before submission.</p>
<p>Authors should upload the electronic versions of their manuscripts (prepared in the <em>Journal of Biosciences</em> format) via the Editorial Manager / eBiosciences at <a href="http://www.editorialmanager.com/jbsc/" target="_blank">http://www.editorialmanager.com/jbsc/</a><br />Figures and photographs should be attached separately either as .jpg / ppt  or bmp files (minimum 300 dpi).</p>
<p>Formatting requirements are as for typescripts (see below).</p>
<p>Submission of a manuscript will be held to imply that the work reported in it is original, that the results have not been previously published (either in part or whole) and are not under consideration for  publication elsewhere; and further, that if accepted, will not be published elsewhere.  Manuscripts that are not written in clear and understandable English are liable to be returned for re-writing even if they may be publishable on the basis of their scientific content.</p>
<p>Authors are invited to suggest names and addresses of four experts who in their opinion can review the paper. The choice of referees will however remain with the editorial board.</p>
<p>No submission charges or  page charges. Colour reproduction of figures at no extra cost.</p>

<h3>Title Page</h3>
<p>The title of the paper must be brief and contain words useful for indexing. Avoid serial titles.</p>
<p>The names of authors with initials and the address of the institution where the work was carried out must be given.</p>
<p>An abbreviated running title of not more than 50 letters (including spaces) must also be given.</p>
<p>The address for communication (along with e-mail address and telephone, fax and mobile numbers, if available), should be given.</p>

<h3>Abstract</h3>
<p>Papers must have an abstract of not more than 200 words summarizing the significant results reported.</p>

<h3>Keywords</h3>
<p>Three to six keywords must be provided.</p>

<h3>Text</h3>
<p>
	The paper must be divided into sections, starting with 'Introduction' and ending with 'Discussion'.<br />
	The main sections should be numbered 1, 2 etc., the sub-sections 1.1, 1.2, etc., and further subsections (if necessary) 1.1.1, 1.1.2, etc.<br />
	All measurements should be given in SI units.<br />
	Avoid numbers at the beginning of a sentence, but if you have to use them, spell them.<br />
	Taxonomic affiliation such as Phylum, Order and Family as well as the common name of the main study organism should be given in the title or at the first<br />
	mention unless such information is likely to<br />
	be obvious to the broad range of  biologists.<br />
	Scientific names of genera and species should be in italics.<br />
	Authority for names of taxa should be cited in the summary and at the first mention of a taxon in the text, but not elsewhere.<br />
	Accepted common names of plants and animals (and other organisms) and of plant and animal or other diseases should neither be capitalized nor placed within quotation marks.<br />
	Words and phrases not of English origin and not in common use (e.g. <em>in vitro, in situ</em>) should be in italics.
</p>

<h3>Tables</h3>
<p>All tables must be numbered consecutively in arabic numerals in the order of appearance in the text.</p>
<p>Tables should be self-contained and have a descriptive title.</p>
<p>Details of experiments (not mentioned in the text) may be indicated in brief below the table as a legend.</p>

<h3>Figures and Photographs</h3>
<p>All figures should be numbered consecutively in arabic numerals in the order of appearance in the text.</p>
<p>Photographs should be sharp, of high contrast with an indication of the scale.</p>
<p>The preferred figure formats are EPS for vector graphics exported from a drawing program and TIFF for halftone illustrations. Scan resolution: Scanned line drawings should be digitalized with a minimum resolution of 800 dpi relative  to the final figure size. For digital halftones, 300 dpi is usually sufficient.</p>
<p>Colour illustrations: Store colour illustrations as RGB (8 bits per channel) in TIFF format. Vector graphics: Fonts used in the vector graphics must be included.  Please do not draw with hairlines.  The minimum line width is 0.2 mm   (i.e.  0.567 pt) relative to the final size.</p>

<h3>References</h3>
<p>References should be cited in the text by author and year, not by number. If there are more than two authors, reference should be made to the first author followed by <em>et al</em> in the text.</p>
<p>References at the end of the paper should be listed alphabetically by authors' names,  followed by initials, year of publication, full title of the paper, name of the journal  (abbreviated according to the World List of Scientific Periodicals, Butterworths, London), volume number, initial and final page numbers.</p>
<p>References to books should include: name(s) of author(s), initial(s), year of publication, title of the book, edition if not the first, initials and name(s) of editor(s) if any, preceded by ed(s), place of publication,  publisher, and pages referred to.</p>
<p>References to thesis must include the year, the title of the thesis, the degree for which submitted and the university.  If there are more than eight authors, then after eighth authors '<em>et al.</em>' could be used</p>
<p><strong>Examples:</strong></p>
<p>Hong-Goka BC and Chang FL 2004 Estrogen receptors alpha and beta in choroid plexus epithelial cells in Alzheimer’s disease. <em>Neurosci. Lett.</em> <strong>360</strong> 113–116</p>
<p>
	Peiter E, Fischer M, Sidaway K, Roberts SK and Sanders D 2005a Multiple RNA surveillance pathways limit aberrant expression of iron uptake mRNAs and prevent Iron<br />
	toxicity in <em>S. cerevisiae. Mol. Cell</em> <strong>19</strong> 39–51
</p>
<p>Ramanna MS and Hermsen JHTh 1979 Genome relationships in tuber-bearing Solanums; in <em>Biology and taxonomy of Solanaceae</em> (eds) JG Hawkes, RN Lester and AG Skelding (London: Academic Press) pp 647–654</p>
<p>Samiwala EB 1987 DNA cloning in Haemophilus influenzae, PhD thesis, University of Bombay, Bombay</p>
<p>Zar JH 1974 Biostatistical analysis (New Jersey: Prentice Hall)</p>

<h3>Abbreviations, Symbols, Units etc.</h3>
<p>The authors should follow internationally agreed rules especially those adopted by the IUPAC-IUB Commission on Biochemical Nomenclature (CBN).</p>
<p>The journal will essentially follow the rules defined in the IUPAC Manua1 of symbols and terminology for physico-chemical quantities and units (Butterworths, London), 1970.</p>
<p>Enzyme names may be abbreviated except on the first occasion, when the full name and abbreviation in parenthesis should be given.</p>

<h3>Footnotes</h3>
<p>Footnotes to the text should be avoided if possible but when essential should be numbered consecutively and typed on a separate sheet.</p>

<h3>Proofs</h3>
<p>Pdf proofs sent to the authors must be returned within 48 hours to the editorial office.</p>
<p>Delayed despatch of corrected proofs will delay publication. In order to minimize the corrections and alterations in the proof stage, authors are requested to prepare the manuscript carefully according to journal format before submitting it for publication.</p>

<h3>Reprints</h3>
<p>20 reprints will be supplied free of charge on request.</p>

<h3>Review Articles</h3>
<p>Both mini-reviews (approximately 1000–1500 words and no more than 2 tables and/or illustrations) and full-length reviews (of no fixed length) are acceptable. Mini-reviews will be processed especially rapidly.</p>
<p>A review should be topical, sufficiently critical and comprehensive with regard to citations to the literature. It should draw attention to outstanding problems and take care to accommodate different points of view. It should stand out from other recently-published reviews on the same theme.</p>
<p>The format requirements for original research articles apply to reviews too.</p>

