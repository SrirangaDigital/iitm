<h1>Proceedings – Mathematical Sciences</h1>
<h2>&nbsp;</h2>
<h3>History</h3>
<p>Earlier a part of the <em>Proceedings of the Indian Academy of Sciences – Section A,</em> which was split, <em>Proceedings - Mathematical Sciences</em> began to be published as an independent theme  journal from 1978.</p>
<h3>Aim and Scope</h3>
<p>This journal publishes papers covering current research in mathematics. Critical reviews of important fields are also published. Papers in applied areas are considered for publication only on the basis of their mathematical content. Special issues devoted to advances in specific areas of mathematics and proceedings of selected high-quality conferences are also published.</p>
<h3>Co-publishing Arrangement with Springer</h3>
<p><em>Proceedings - Mathematical Sciences</em> is now distributed in print outside India and online worldwide by <a target="_blank" href="http://www.springer.com">Springer</a>, co-publisher of the journal together with the Indian Academy of Sciences. On Springer, <em>Proceedings - Mathematical Sciences</em> <a target="_blank" href="http://www.springer.com/12044">(www.springer.com/12044)</a> is part of <a target="_blank" href="http://www.springerlink.com">SpringerLink</a>, one of the world's leading interactive databases of high quality STM journals, book series, books, reference works and online archives collection. Print subscriptions within India and open online access will continue to be served from Indian Academy of Sciences.</p>
