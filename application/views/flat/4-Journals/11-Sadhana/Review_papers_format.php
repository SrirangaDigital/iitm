<h1>Review Article Style and Format: Manuscript Preparation</h1>
<h2>Sadhana</h2>
<p>The article should review current status of technology and research and the content should be analytical. The audience are students to researchers in engineering sciences. The article should be helpful to the readers in terms of introduction and references for their future work.</p>
<h3>Title</h3>
<p>Title should reflect the topic and contents of the review paper.</p>
<h3>Abstract</h3>
<p>The abstract must be a single paragraph summarizing the current status of the knowledge within the area reviewed and the conclusions on future research.</p>
<h3>Introduction</h3>
<p>This should provide a background to the review which focuses on work published over the last few years that has advanced our understanding of the topic under consideration. This can be of tutorial nature.</p>
<h3>Current status of knowledge or Methods and Results</h3>
<p>Reviews should include a "Current status of knowledge" section. This section of the review should focus on research efforts that have advanced our knowledge on this topic and should carefully deal with divergent hypotheses if they exist.</p>
<h3>Applications</h3>
<p>Real world applications would help the readers the importance of the topic.</p>
<h3>Conclusions</h3>
<p>Conclusions, discussion, problems, or areas for future research may be included.</p>
