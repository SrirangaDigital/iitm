<h3>Abstract</h3>
<p>All papers must have an Abstract of not more than 200 words. Presentation of numerical results should be avoided as far as possible. If the manuscript is accepted, a Graphical Abstract <a href="#example">(See examples below)</a> consisting of text not more than 50 words and a suitable figure or equation is required.</p>

<h3>Keywords</h3>
<p>3 to 6 Keywords must be provided for indexing and information retrieval.</p>

<h3>Text</h3>
<p>The manuscripts of Regular research articles must be divided into sections consisting of Introduction, Experimental or Theoretical/Computational, Results and Discussion, and Conclusions. Main sections should be numbered 1, 2 etc., sub-sections as 1.1, 1.2 etc. and further sub-sections as 1.1a, 1.1b etc. or 1.1.1, 1.1.2, etc. Use of SI system of units and symbols is recommended.</p>

<h3>Tables</h3>
<p>All Tables must be numbered consecutively in Arabic numerals in the order of their appearance in the text. Tables should be selfcontained and have a descriptive title.</p>

<h3>Figures</h3>
<p>All Figures including photographs should be numbered consecutively in Arabic numerals in the order of their appearance in the text. Figure captions must be also typed on a separate sheet.</p>
<p>Line drawings and structures should be made using suitable software like ChemDraw, IsisDraw, etc. Lines must be sufficiently thick for reduction to a half or third of the original size (0.3 mm for axes and 0.6 mm for curves are suggested).</p>

<h3>Symbols and Mathematical materials</h3>
<p>Symbols and equations should be carefully checked by authors for correctness in the manuscript and also in the PDF file prepared by Editorial Manager.</p>
<p>Equations must be clearly written, each on its own line, well separated from the text. All equations must be numbered consecutively in Arabic numerals. Authors must check wherever special characters (Greek letters, vector, tensor, matrix etc. symbols) are used. For example, vectors must be in bold letters. If special characters are used extensively then an explanatory table of Symbols in a separate page is recommended.</p>

<h3>References</h3>
