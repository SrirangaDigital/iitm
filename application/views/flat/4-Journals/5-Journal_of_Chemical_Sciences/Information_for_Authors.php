<h3>Supplementary Information (SI)</h3>
<p>Authors are encouraged to submit extensive Data/Figures/Tables and scanned copies of original spectra in Supplementary Information. For newly synthesized molecules, it is mandatory to submit scanned copies one or more of the following spectra/reports, as applicable: NMR (1H, 13C) spectra; ESR spectra; CIF (converted to WORD file) and CHECK CIF files for the crystal structural analysis (in addition to CCDC numbers); mass spectra; combustion analysis reports; etc. Figures and Tables in SI are to be labeled as S1, S2, etc. The content of SI shall be described briefly in a paragraph in the manuscript after 'Conclusions'. See a Sample file in Journal URL.</p>

<h3>Appendices</h3>
<p>if any, should be labeled A, B, ... and equations as A1, A2, ..B1, B2,...</p>

<h3>Proofs</h3>
<p>Authors should prepare the original/revised manuscript carefully in order to minimize corrections and alterations at the proof stage. Proofs will be sent as PDF files to the Corresponding Author. Corrections should be sent by email either as a serial list of page, column, para and line numbers or as annotated PDF file.</p>

