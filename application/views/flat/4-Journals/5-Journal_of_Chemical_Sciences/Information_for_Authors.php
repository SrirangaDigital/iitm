<h1>Information for Authors</h1>
<h2>Journal of Chemical Sciences</h2>

<p>Authors should follow the manuscript submission instructions in website <a href="http://www.editorialmanager.com/jcsc/" target="_blank">http://www.editorialmanager.com/jcsc/</a>. Manuscript should be prepared in grammatically correct English.</p>
<p>Papers must present results of original work as a Regular Article or Rapid Communication. Critical Reviews of important fields and Perspective articles in emerging areas will also be considered for publication. Authors must also submit a covering letter explaining the novelty of the work, mandatory for Rapid Communication, and suggest 2 to 3 names of Referees. Submission of the manuscript will be held to imply that it has not been previously published and it is not under consideration for publication elsewhere; and further that, if accepted, it will not be published elsewhere. The Jurisdiction for all disputes concerning submitted articles, published materials, subscription and sale will be at courts/tribunals situated in Bengaluru City only.</p>
<h3>Typescript</h3>
<p>Text should be typed double spaced with 1" margin on all sides in Microsoft Word Format along with embedded Figures, Captions and Tables distinctly separated from the main text. Please consult editorial staff at 'chemsci@ias.ernet.in', before submitting manuscript in any other format.</p>
