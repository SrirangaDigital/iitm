<h1>Journal of Chemical Sciences</h1>
<h2>Formerly: Proceedings of the Indian Academy of Sciences (Chemical Sciences)</h2>
<h3>History</h3>
<p>Originally a  part of the <em>Proceedings of the Indian Academy of Sciences – Section A,</em> started  in 1934, the journal evolved into an independent journal titled ‘Proceedings – Chemical Sciences’ in 1978 . It was retitled ‘Journal of Chemical Sciences’ in 2004.</p>
<h3>Aim and Scope</h3>
<p>The journal publishes original research articles and rapid communications covering all areas of chemical sciences.  It also publishes special issues of conference proceedings/symposia on frontier areas of the subject.</p>
<h3>Co-publishing Arrangement with Springer</h3>
<p><em>Journal of Chemical Sciences</em> is now distributed in print outside India and online worldwide by <a target="_blank" href="http://www.springer.com">Springer</a>, co-publisher of the journal together with the Indian Academy of Sciences. On Springer, <em>Journal of Chemical Sciences</em> <a target="_blank" href="http://www.springer.com/12039">(www.springer.com/12039)</a> is part of <a target="_blank" href="http://www.springerlink.com">SpringerLink</a>, one of the world's leading interactive databases of high quality STM journals, book series, books, reference works and online archives collection. Print subscriptions within India and open online access will continue to be served from Indian Academy of Sciences.</p>
