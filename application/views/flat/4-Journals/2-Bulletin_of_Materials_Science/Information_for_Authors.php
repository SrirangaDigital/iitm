<h1>Information for Authors</h1>
<h2>Bulletin of Materials Science</h2>

<p>Authors may communicate their research work as papers and rapid communications to the Bulletin of Materials Science. They may also submit topical reviews.</p>
<p>We have now switched over to an Editorial Management System.</p>

<h3>Manuscript Submission</h3>
<p>Authors are required to submit manuscripts online to<br />
<a href="http://www.editorialmanager.com/boms" target="_blank">http://www.editorialmanager.com/boms</a></p>

<p>It will have the advantage that the author(s) would be able to track their paper through the refereeing process. If you have any difficulty, you may contact the editorial office and they will be happy to answer any questions that you may have.</p>
<p>Submission of a script will be held to imply that it has not been previously published and is not under  consideration for publication elsewhere; and further that, if accepted, it will not be published elsewhere.</p>

<h3>Typescript</h3>
<p>Papers must be typed double spaced with ample margin on all sides. Text with embedded graphics should be in Microsoft Word Format.</p>

<h3>Title page</h3>
<ol>
	<li class="gap-above-small">The title of the paper must be brief but informative. Serial titles are to be avoided.</li>
	<li class="gap-above-small">The names with initials of authors and the name and address of the institution where the work was done must be given.</li>
	<li class="gap-above-small">An abbreviated running title of not more than 50 letters and spaces must also be given.</li>
	<li class="gap-above-small">Authors are requested to provide e-mail and fax addresses while submitting the script online.</li>
</ol>

<h3>Abstract</h3>
<p>Papers must have a brief abstract of the significant results reported in the paper.</p>

<h3>Keywords</h3>
<p>Between 3 and 6 keywords must be provided for indexing and information retrieval.</p>

<h3>The Text</h3>
<p>The paper must be divided into sections starting preferably with 'Introduction' and ending with 'Conclusions'.</p>
<p>The main sections should be numbered 1, 2 etc, the sub-sections 1.1, 1.2, etc, and further sub-sections (if necessary) 1.1a, 1.1b, etc.</p>
<p>In lists of three or more items, do not place a comma after the penultimate item (red, blue and green), except where necessary to avoid ambiguity (e.g. genetics, neuroscience, molecular and cell biology, and scuba diving).</p>
<p>Use single quote.</p>

<h3>Tables</h3>
<p>All tables must be numbered consecutively in arabic numerals in the order of occurrence in the text; they should be selfcontained and have a descriptive title.</p>

<h3>Figures</h3>
<p>All figures, including photographs should be numbered consecutively in Arabic numerals in the order of occurrence in the text. Figure captions must be typed on a separate sheet. Curves should be drawn sufficiently thick to permit reduction to one half or even one third of the original size. The axes and letterings must be about 0.3 mm thick and curves 0.6 mm thick. Whenever computer outputs are directly submitted as figures, they should conform to the same specifications given earlier for drawings.</p>
<p>Decide if the figure is to be a one-column figure (maximum width 8.5 cm, including all labels and legends) or a two-column figure (maximum width 17.5 cm, including all labels and legends). An obvious advantage of doing this is that it helps reduce the work to be done when revising the manuscript or communicating the final version to the publishing office of the journal. A more subtle advantage, however, is that the reviewers (and the editor) see exactly what the reader will see in the printed journal, making it possible for them to spot potential difficulties that a reader may face in interpreting the figures. Preparing figures initially to the expected final size also avoids the need for the publisher to enlarge or reduce the figure to fit the journal requirements, thereby maintaining the original quality. Given the quality of modern laser printers and the relative ease, compared to even a few years ago, of obtaining laser printouts of figures, providing the publisher with camera-ready figures or electronic copies ensures better figures in print.</p>
<p>Use Helvetica as the font for legends and labels. Ensure that no label or legend is smaller than 8 pt. All lines must be at least 0.5 pt (no hairline rules).</p>
<h4>Artwork Preparation</h4>
<p>Electronic Figure Submission</p>
<ul>
	<li>Supply all figures electronically.</li>
	<li>For vector graphics, the preferred format is EPS; for halftones, please use TIFF format. MS Office files are also acceptable.</li>
	<li>Vector graphics containing fonts must have the fonts embedded in the files.</li>
	<li>Name your figure files with "Fig" and the figure number, e.g., Fig1.eps.</li>
</ul>
<p>Line Art</p>
<ul>
	<li>Definition: Black and white graphic with no shading.</li>
	<li>Do not use faint lines and/or lettering and check that all lines and lettering within the figures are legible at final size.</li>
	<li>All lines should be at least 0.1 mm (0.3 pt) wide.</li>
	<li>Line drawings should have a minimum resolution of 1200 dpi.</li>
</ul>
<p>Halftone Art</p>
<ul>
	<li>Definition: Photographs, drawings, or paintings with fine shading, etc.</li>
	<li>If any magnification is used in the photographs, indicate this by using scale bars within the figures themselves.</li>
	<li>Halftones should have a minimum resolution of 300 dpi.</li>
</ul>
<p>Combination Art</p>
<ul>
	<li>Definition: a combination of halftone and line art, e.g., halftones containing line drawing, extensive lettering, color diagrams, etc.</li>
	<li>Combination artwork should have a minimum resolution of 600 dpi.</li>
</ul>
<p>Color Art</p>
<ul>
	<li>If the figures will be printed in black and white, do not refer to color in the captions.</li>
	<li>Color illustrations should be submitted as RGB (8 bits per channel).</li>
</ul>
<p>Figure Lettering</p>
<ul>
	<li>To add lettering, it is best to use Helvetica or Arial (sans serif fonts).</li>
	<li>Keep lettering consistently sized throughout your final-sized artwork, usually about 2-3 mm (8-12 pt).</li>
	<li>Variance of type size within an illustration should be minimal, e.g., do not use 8-pt type on an axis and 20-pt type for the axis label.</li>
	<li>Avoid effects such as shading, outline letters, etc.</li>
	<li>Do not include titles or captions within your illustrations.</li>
</ul>
<p>Figure Numbering</p>
<ul>
	<li>All figures are to be numbered using Arabic numerals.</li>
	<li>Figures should always be cited in text in consecutive numerical order.</li>
	<li>Figure parts should be denoted by lower case letters (a, b, c, etc.).</li>
	<li>If an appendix appears in your article and it contains one or more figures, continue the consecutive numbering of the main text. Do not number the appendix figures, “A1, A2, A3, etc."</li>
</ul>
<p>Figure Captions</p>
<ul>
	<li>Each figure should have a concise caption describing accurately what the figure depicts.</li>
	<li>Figure captions begin with the term Figure in bold type, followed by the figure number, also in bold type.</li>
	<li>Identify all elements found in the figure in the figure caption; and use boxes, circles, etc., as coordinate points in graphs.</li>
	<li>Identify previously published material by giving the original source in the form of a reference citation at the end of the figure caption.</li>
	<li>Figure Placement and Size</li>
	<li>When preparing your figures, size figures to fit in the column width.</li>
</ul>
<p>Permissions</p>
<p>If you include figures that have already been published elsewhere, you must obtain permission from the copyright owner(s) for both the print and online format. Please be aware that some publishers do not grant electronic rights for free and that Springer will not be able to refund any costs that may have occurred to receive these permissions. In such cases, material from other sources should be used.</p>
<h3>Units</h3>
<p>Unit dimensions should not be expressed using a solidus but using negative integers or the word ‘per’. That is, do not use mm/min but <strong>mm min<sup>–1</sup></strong>. Do not use units mg protein<sup>–1</sup>, which is unclear but use as <strong>mg per protein</strong>.</p>
<p>Units and associated symbols must invariably follow SI practice.</p>

<h3>References</h3>
<p>References are numbered consecutively in the order in which they are first mentioned in the text. Identify references in text, tables, and legends by square bracketed numbers [1], and provide a list of references at the end of the article in numerical order using square brackets for the numbers. Reuse the original number assigned to the reference each time the reference is cited in the text, regardless of its previous position in the text. Insert the citation numbers at the relevant place in the text. As a general rule, reference numbers should be placed inside full stops, commas, colons and semicolons.</p>
<p>When citing multiple references, use commas (without spaces) to separate them. Use an en-dash to join inclusive first and last numbers, e.g. [2,3,4,5,7,10] would be abbreviated to [2–5,7,10].</p>

<h3>Footnotes</h3>
<p>Footnotes must be avoided if possible but when necessary should be numbered consecutively, and typed on a separate sheet.</p>

<h3>Proofs</h3>
<p>Authors are requested to prepare the manuscript carefully before submitting it for publication to minimize corrections and alterations in the proof which increase publication costs. PDF proof will be sent to the author by email. The author should send it back with corrections/clarifications, if any, by email.</p>

