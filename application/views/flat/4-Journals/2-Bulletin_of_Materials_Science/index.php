<h1>Bulletin of Materials Science</h1>
<h2>&nbsp;</h2>
<h3>History</h3>
<p>The <em>Bulletin of Materials Science</em> began in the year 1979. The journal is published by the Indian Academy of Sciences in collaboration with the Materials Research Society of India and the Indian National Science Academy.</p>
<h3>Aim and Scope</h3>
<p>The journal publishes research and review articles,  and rapid communications in all areas of materials science. The journal also publishes important conference/ symposia proceedings that are of interest to materials scientists.</p>
<h3>Co-publishing Arrangement with Springer</h3>
<p><em>Bulletin of Materials Science</em> is now distributed in print outside India and online worldwide by <a target="_blank" href="http://www.springer.com">Springer</a>, co-publisher of the journal together with the Indian Academy of Sciences. On Springer, <em>Bulletin of Materials Science</em> <a target="_blank" href="http://www.springer.com/12034">(www.springer.com/12034)</a> is part of <a target="_blank" href="http://www.springerlink.com">SpringerLink</a>, one of the world's leading interactive databases of high quality STM journals, book series, books, reference works and online archives collection. Print subscriptions within India and open online access will continue to be served from Indian Academy of Sciences.</p>
