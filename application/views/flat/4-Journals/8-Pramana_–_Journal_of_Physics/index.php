<h1>Pramana</h1>
<h2>Journal of Physics</h2>
<h3>History</h3>
<p><em>Pramana – Journal of Physics</em> was launched in July 1973. <em>Pramana</em> (which in Sanskrit means “source of valid knowledge, a standard”) is the outcome of a nationwide effort by Indian physicists to disseminate their best efforts in Physics. The journal is published in collaboration with the Indian National Science Academy and Indian Physics Association.</p>
<h3>Aim and Scope</h3>
<p>The journal publishes current research in physics, both original contributions – research papers, brief reports or rapid communications – and invited reviews. Pramana also publishes special issues devoted to advances in specific areas of physics and proceedings of select high-quality conferences and theme issues.</p>
<h3>Co-publishing Arrangement with Springer</h3>
<p><em>Pramana – Journal of Physics</em> is now distributed in print outside India and online worldwide by <a target="_blank" href="http://www.springer.com">Springer</a>, co-publisher of the journal together with the Indian Academy of Sciences. On Springer, <em>Pramana – Journal of Physics</em> <a target="_blank" href="http://www.springer.com/12043">(www.springer.com/12043)</a> is part of <a target="_blank" href="http://www.springerlink.com">SpringerLink</a>, one of the world's leading interactive databases of high quality STM journals, book series, books, reference works and online archives collection. Print subscriptions within India and open online access will continue to be served from Indian Academy of Sciences.</p>
