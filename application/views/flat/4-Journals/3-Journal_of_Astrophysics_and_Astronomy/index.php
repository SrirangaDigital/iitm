<h1>Journal of Astrophysics and Astronomy</h1>
<h2>&nbsp;</h2>
<h3>History</h3>
<p>Since 1977, papers in Astrophysics and Astronomy appeared as a special section in Pramana. The success of this was the impetus to launch, in 1980, the <em>Journal of Astrophysics and Astronomy.</em></p>
<h3>Aim and Scope</h3>
<p>The journal publishes original research papers on all aspects of astrophysics and astronomy, including instrumentation, laboratory astrophysics, and cosmology. Critical reviews of topical fields are also published.</p>
