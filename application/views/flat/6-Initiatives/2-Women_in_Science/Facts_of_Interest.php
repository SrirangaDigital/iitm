<h3>Prof. Indira Nath : Simply a Class Apart</h3>
<p>Prof. Indira Nath is the first Indian Woman to win the L'Oreal-UNESCO 2002 award for "Women in Science - Asia/Pacific region". She is also a recepient of Padma Shri (1999) for her contribution to the field of immunology and is considered a world renowed authority on leprosy. She is also a Fellow of the Indian Academy of Sciences since 1988.</p>
