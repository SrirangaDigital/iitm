<p>The issue of under representation of women in science is being seen with a great deal of concern all over the world. Over the years, several Academy Fellows had suggested a need to address this concern. In order to examine and study â€“ the current status with regard to women in science in the Indian context; the factors influencing the science career for Indian women; and to suggest recommendations, the Council of the Indian Academy of Sciences had in January 2003 constituted a <a href="Members_of_the_Committee">committee</a> on "Women in Science". This committee had come up with many initiatives and action plan for consideration by the Council and Fellowship. This led to the formation of the <a href="Panel_Members">Panel</a> for "Women in Science" (WiS), in January 2005, to carry out recommendations made by the committee and to suggest measures for obtaining suitable solutions.</p>
<p>The Panel is currently chaired by Prof. Rohini M Godbole. The Panel has undertaken several <a href="Initiatives">initiatives</a> towards fulfilling its objectives.</p>
