<h4>RESTART Programme by GE</h4>
<p>General Electric's (GE's) John F Welch Technology Centre (JFWTC), Bengaluru has launched a programme called RESTART which focuses exclusively on hiring women scientists and engineers who have taken a career break and now want to get back to work. The programme offers various facilities and opportunities for women scientists and engineers to restart their career and work in their centre.</p>
