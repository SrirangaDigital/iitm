<h4>DST Task Force on Women in Science</h4>
<p>The DST Task Force on Women in Science is maintaining a directory of Indian Women who have taken up studies in Science/Agriculture, Engineering and Medicine. The directory provides a resource pool of Women Scientists, Engineers and Doctors who have registered with them. The registrants are not necessarily Ph.D holders and are also those who are pursuing Ph.Ds.</p>
