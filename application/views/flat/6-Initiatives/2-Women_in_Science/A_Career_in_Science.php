<h1>A Career in Science</h1>
<h2>&nbsp;</h2>
<p>Women in science comprise only a small percentage of the total number of working women in India. Although there is no explicit discrimination against women at the various levels of education, attitudinal biases and absence of supportive institutional/social structures have, over the years, operated as powerful forces against talented women realizing their full potential in the pursuit of productive and rewarding careers in science.</p>
<p>The under-representation of women in science, particularly at the senior levels of teaching and research in India, has become a serious cause of concern for women scientists and women policy planners. Hence there is a need to make women aware of a career in science as a possible option of career and then to retain them in the profession.</p>
<p>In this direction Women in Science Panel has started a new initiative of conducting a series of seminars/ lectures on the topic "Women in Science : A Career in Science". This is a part of their Role Model Program. These seminars will have presentations by leading women scientists about latest developments in their area of activity, to showcase the work done by women scientists to an audience of both the genders.</p>
<p>The main objectives of this initiative are as follows:</p>
<ul>
	<li>To inspire and motivate young women to take up a career in Science.</li>
	<li>To create an awareness on various career options available to young women scientists.</li>
	<li>To explore avenues for entrepreneur development for women through Science.</li>
</ul>
