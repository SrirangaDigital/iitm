<h1>Academy Committee on Scientific Values</h1>
<h2>&nbsp;</h2>

<p>The Council of the Academy had in 2003 constituted a Committee to consider and submit a report on ethical guidelines which the fellowship of the Academy should follow.</p>
<p>The Committee submitted its report to the Council, which at its meeting in December 2005 approved <a href="<?=RESOURCES_URL?>Other_Publications/Overview/scival_report.pdf" target="_blank">this report</a> as an Academy Document.</p>

